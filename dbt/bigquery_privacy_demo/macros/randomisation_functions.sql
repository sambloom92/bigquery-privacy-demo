{% macro random_numeric_id(digits) %}
lpad(cast(cast(ROUND(RAND() * (pow(10, {{ digits }}) - 1)) as int) as string), {{ digits }}, '0')
{% endmacro %}

{% macro random_normal_distribution(mean, stdev) %}
{{ mean }} + {{ stdev }}*(sqrt( 2*abs(log( RAND()))))*cos( 2*ACOS(-1)*RAND())
{% endmacro %}

{% macro random_gbp_amount(mean_amount, stdev_amount) %}
    floor(
        greatest(
            {{ random_normal_distribution(mean_amount, stdev_amount) }},
            0) * 100
        ) / 100
{% endmacro %}

{% macro random_choice(choices) %}
{%- set str_choices %}
[{% for choice in choices %}'{{ choice }}'{% if not loop.last %},{% endif %}{% endfor %}]
{%- endset %}
{{ str_choices }}[cast(floor(rand() * array_length({{ str_choices }})) as int)]
{% endmacro %}

{% macro random_date(min_date, max_date) %}
DATE_FROM_UNIX_DATE(CAST(UNIX_DATE('{{ min_date }}') + (UNIX_DATE('{{ max_date }}')) -  UNIX_DATE('{{ min_date }}') * RAND() AS INT64))
{% endmacro %}

{% macro random_time(min_time, max_time) %}
TIME_ADD('{{ min_time }}', INTERVAL cast(TIME_DIFF('{{ min_time }}', '{{ max_time }}', MILLISECOND) * RAND() as int) MILLISECOND)
{% endmacro %}

{% macro random_datetime(min_datetime, max_datetime) %}
DATETIME(
    {{ random_date(DATE('{{ min_datetime }}'), DATE('{{ max_datetime }}')) }},
    {{ random_time('00:00:00', '23:59:59') }}
  )
{% endmacro %}

