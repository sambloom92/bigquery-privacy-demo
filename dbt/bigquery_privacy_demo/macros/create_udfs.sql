{% macro create_udfs(schema) %}
CREATE OR REPLACE FUNCTION `{{ schema }}`.random_numeric_id(digits INT)
RETURNS String
AS (
    lpad(cast(cast(ROUND(RAND() * (pow(10, digits) - 1)) as int) as string), digits, '0')
);

CREATE OR REPLACE FUNCTION `{{ schema }}`.random_normal_distribution(mean FLOAT64, stdev FLOAT64)
RETURNS FLOAT64
AS (
        mean + stdev*(sqrt( 2*abs(log( RAND()))))*cos( 2*ACOS(-1)*RAND())
    )
;

CREATE OR REPLACE FUNCTION `{{ schema }}`.random_gbp_amount(mean_amount FLOAT64, stdev_amount FLOAT64)
RETURNS FLOAT64
AS (
    floor(
        greatest(
            `{{ schema }}`.random_normal_distribution(mean_amount, stdev_amount),
            0) * 100
        ) / 100
);

CREATE OR REPLACE FUNCTION `{{ schema }}`.random_choice(choices array<String>)
RETURNS String
AS (
    choices[cast(floor(rand() * array_length(choices)) as int)]
);

CREATE OR REPLACE FUNCTION `{{ schema }}`.random_date(min_date Date, max_date Date)
RETURNS Date
AS (
    DATE_FROM_UNIX_DATE(CAST(UNIX_DATE(min_date) + (UNIX_DATE(max_date)) -  UNIX_DATE(min_date) * RAND() AS INT64))
);

CREATE OR REPLACE FUNCTION `{{ schema }}`.random_time(min_time Time, max_time Time)
RETURNS TIME
AS (
    TIME_ADD(min_time, INTERVAL cast(TIME_DIFF(min_time, max_time, MILLISECOND) * RAND() as int) MILLISECOND)
);

CREATE OR REPLACE FUNCTION `{{ schema }}`.random_datetime(min_datetime DATETIME, max_datetime DATETIME)
RETURNS DATETIME
AS (
    DATETIME(
        `{{ schema }}`.random_date(DATE(min_datetime), DATE(max_datetime)),
        `{{ schema }}`.random_time(PARSE_TIME('%T', '00:00:00'), PARSE_TIME('%T', '23:59:59'))
        )
);

